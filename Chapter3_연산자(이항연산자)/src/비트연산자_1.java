/*
 *    & (*), | (+), ^
 *    
 *    ==> 2진법으로 변환후 연산 ==> 정수는 32bit
 *    --------------------------------------
 *    ------------------------------------------------
 *               &(*)     |(+)      ^(비트가 다르다)
 *    ------------------------------------------------
 *    00          0        0         0
 *    ------------------------------------------------
 *    01          0        1         1
 *    ------------------------------------------------
 *    10          0        1         1
 *    ------------------------------------------------
 *    11          1        1         0
 *    ------------------------------------------------
 *    
 *    25   &   10              25     |    10            25     ^    10
 *    --       --              --          --            --          --
 *    11001   01010            11001      01010          11001      01010
 *    
 *    11001                    11001                     11001
 *    01010                    01010                     01010
 *    -----&                   -----|                    -----^
 *    01000 ==> 8              11011 ==> 27              10011 ==> 19
 */
public class 비트연산자_1 {

	public static void main(String[] args) {
		// TODO Auto-generated method stub
		System.out.println(25&10);
		System.out.println(25|10);
		System.out.println(25^10);
		
		System.out.println(36&25);
		System.out.println(36|25);
		System.out.println(36^25);
		
		System.out.println(67&45);
		System.out.println(67|45);
		System.out.println(67^45);

	}

}
